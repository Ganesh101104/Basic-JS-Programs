let n1=45;
let n2=69;
let operator="+";
switch(operator){
    case "+":
        console.log(n1+n2);
        break;
    case "-":
        console.log(n1-n2);
        break;
    case "*":
        console.log(n1*n2);
        break;
    case "/":
        console.log(n1/n2);
        break;
    default:
        console.log("Invalid operator");
}